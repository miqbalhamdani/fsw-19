const a = 10;
const b = '10';

const hasil = a + b;

console.log(hasil); // 1010

console.log(typeof a); // Number

console.log(typeof b); // String

console.log(a === b); // false, karena a bertipe data String dan b bertipe data Number

console.log(a == b); // true, karena value nya sama sama 10, walaupun tipe data nya berbeda
